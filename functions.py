def add(num1, num2):
    print('{} + {}'.format(num1, num2))
    print(num1 + num1)

def subtract(num1, num2):
    print('{} - {} ='.format(num1, num2))
    print(num1 - num2)
    
def divide(num1, num2): 
    print('{} / {} = '.format(num1, num2))
    print(num1 / num2)

def multiply(num1, num2):
    print('{} * {} = '.format(num1, num2))
    print(num1 * num2)